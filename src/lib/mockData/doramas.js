// Helper functions ready to be replaced with Supabase queries
export const getAllDoramas = async () => {
  return new Promise((resolve) => {
    setTimeout(() => {
      resolve(mockDoramas);
    }, 300);
  });
};

export const getFeaturedDoramas = async () => {
  return new Promise((resolve) => {
    setTimeout(() => {
      resolve(mockDoramas.filter(d => d.featured));
    }, 300);
  });
};

export const getDoramaById = async (id) => {
  return new Promise((resolve, reject) => {
    setTimeout(() => {
      const dorama = mockDoramas.find(d => d.id === id);
      if (dorama) {
        resolve(dorama);
      } else {
        reject(new Error('Dorama not found'));
      }
    }, 300);
  });
};

export const searchDoramas = async (query) => {
  return new Promise((resolve) => {
    setTimeout(() => {
      const results = mockDoramas.filter(d => 
        d.title.toLowerCase().includes(query.toLowerCase()) ||
        d.description.toLowerCase().includes(query.toLowerCase()) ||
        d.genre.some(g => g.toLowerCase().includes(query.toLowerCase()))
      );
      resolve(results);
    }, 300);
  });
};